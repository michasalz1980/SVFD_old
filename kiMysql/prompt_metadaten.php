Die JSON beschreibt alle verfügbaren Tabellen, deren Spalten und die jeweilige Bedeutung:

{metadata_json}

Nutzerfrage:
"{question}"

Ziel:
- Gib **nur** einen ausführbaren MySQL-SQL-Befehl zurück (z. B. SELECT …)
- Verwende ausschließlich die in der JSON definierten Tabellennamen und Spaltennamen
- Gib keine Erklärungen, Kommentare oder Codeblöcke zurück
- Keine generischen Namen wie „Tabelle“ oder „Spalte“ verwenden

Gib nur den SQL-Befehl als Antwort zurück.
